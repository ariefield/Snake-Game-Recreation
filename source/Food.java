//Purpose:Class for the food
public class Food
{
   private int x;
   private int y;
   
   //Constructor
   public Food()
   {
      x = 0;
      y = 0;
   }
   
   //Returns x coordinate
   public int getX()
   {
      return x;
   }
   
   //Returns x coordinate
   public int getY()
   {
      return y;
   }
   
   //Sets a new X coordinate
   public void setX(int x)
   {
      this.x = x;
   }
   
   //Sets a new Y coordinate
   public void setY(int y)
   {
      this.y = y;
   }
}